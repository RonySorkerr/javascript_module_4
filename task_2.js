// even number
let fNumber = 206;
while(fNumber <= 311){
    if(fNumber % 2 === 0){
        console.log('The even number is : ' , fNumber);
    }
    fNumber++;
}
// odd numbers
let sNumber = 61;
while(sNumber <= 100){
    if(sNumber % 2 === 1){
        console.log('The odd number is : ' , sNumber);
    }
    sNumber++;
}